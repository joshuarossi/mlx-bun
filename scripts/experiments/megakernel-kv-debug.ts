import { loadModelConfig } from "../../src/config";
import { Weights } from "../../src/weights";
import { MiniCPM5Model } from "../../src/model/minicpm5";
import { MegakernelRunner } from "../../src/model/megakernel-kernel";
import { SNAPSHOT_MINICPM5 } from "../../tests/paths";
import { Dtype } from "../../src/mlx/ffi";
const model = new MiniCPM5Model(await Weights.open(SNAPSHOT_MINICPM5), await loadModelConfig(SNAPSHOT_MINICPM5));
const g = await Bun.file("goldens/minicpm5-kv-parity.json").json();
const r = new MegakernelRunner(model);
r.decodeStep(g.prompt_ids[0]).dispose();
r.decodeStep(g.prompt_ids[1]).dispose();
const KVDIM = r.KVDIM, sbW = r.sbWidth;
console.log("kvBits[0]=", r.layout.kvBits[0], "kvBits[1]=", r.layout.kvBits[1], "sbWidth=", sbW, "KVDIM=", KVDIM);
const kc = r.kcache.astype(Dtype.float32).toFloat32();
console.log("L0 pos0 kcache[0..8] (q ints):", Array.from(kc.slice(0, 8)).map((x) => x.toFixed(0)));
console.log("L0 pos0 kcache max over KVDIM:", Math.max(...Array.from(kc.slice(0, KVDIM))), "min:", Math.min(...Array.from(kc.slice(0, KVDIM))));
const sb = r.sb.astype(Dtype.float32).toFloat32();
console.log("L0 pos0 sb[0..16]:", Array.from(sb.slice(0, sbW)).map((x) => x.toFixed(4)));
r.dispose();
